# True if condition else False

x = 5
print("Positive" if x >0 else "Negative")

y = -5
print("Positive" if y >0 else "Negative")

num = 6
print("Even Number" if num % 2 == 0 else "Odd Number")

age = 18
print("Adult" if age >=18 else "Child")

temp = 35
print("Hot!" if temp >=18 else "Cold")

user = "admin"
print("Full Access" if user == "admin" else "Not Full Access")